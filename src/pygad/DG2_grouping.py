# ---------------------------------------------------------------------------
# Machine epsilon  (IEEE 754 double precision)
# ---------------------------------------------------------------------------
MACHINE_EPSILON = np.finfo(float).eps          # ≈ 2.220446e-16
GAMMA2 = 2 * MACHINE_EPSILON / (1 - 2 * MACHINE_EPSILON)   # γ₂  (Theorem S.2)

CYCLE_LENGTH = 90  # seconds

# Per-phase-type bounds and minima
PHASE_BOUNDS = {
    "green":  (24, 85),
    "yellow": ( 3,  6),
    "red":    ( 5, 85),
}


# ---------------------------------------------------------------------------
# Phase-type helper
# ---------------------------------------------------------------------------

def _phase_type(state: str) -> str:
    """
    Infer phase type from SUMO state string.

    Counts G/g (green), y (yellow), r/R (red) characters and returns the
    majority type.  Ties broken in priority order: green > yellow > red.

    Examples
    --------
    "GGGG"   -> "green"
    "yyyy"   -> "yellow"
    "rrrr"   -> "red"
    "GGrr"   -> "green"   (2 G vs 2 r — green wins tie)
    "rrGG"   -> "green"
    """
    s = state.lower()
    counts = {
        "green":  s.count("g"),
        "yellow": s.count("y"),
        "red":    s.count("r"),
    }
    # Priority order for ties: green > yellow > red
    for ptype in ("green", "yellow", "red"):
        if counts[ptype] == max(counts.values()):
            return ptype
    return "red"  # fallback (unreachable)


# ---------------------------------------------------------------------------
# Top-level worker — must be a plain module-level function so that
# ProcessPoolExecutor can pickle it across processes.
# ---------------------------------------------------------------------------

def _eval_probe(args):
    """
    Evaluate f at a single probe point.

    Parameters
    ----------
    args : (f, x, tag)
        f   — the fitness callable
        x   — the probe vector (numpy array)
        tag — an opaque identifier returned unchanged so the caller can
              route the result back to the right cell in F or f_hat.
              Convention:
                ("base",)        -> f_base
                ("single", i)   -> f_hat[i]
                ("double", i, j) -> F[i, j]

    Returns
    -------
    (tag, float)
    """
    f, x, tag = args
    return tag, float(f(x))


# ---------------------------------------------------------------------------
# Step 1 - Interaction Structure Matrix  (Algorithm 2, parallelised)
# ---------------------------------------------------------------------------

def _compute_ISM_parallel(
    f: Callable[[np.ndarray], float],
    n: int,
    x_lower: np.ndarray,
    x_upper: np.ndarray,
    n_workers: int,
) -> tuple[np.ndarray, np.ndarray, np.ndarray, float, int]:
    """
    Build the raw Interaction Structure Matrix Lambda using parallel evaluation.

    All unique probe points are submitted in a single batch to a
    ProcessPoolExecutor.  The only serial step is the final assembly of Lambda
    from the collected results, which is pure numpy and negligible.

    Returns
    -------
    Lambda   : (n, n) raw |delta1 - delta2| values
    F        : (n, n) f(x with dims i and j both perturbed)
    f_hat    : (n,)   f(x with only dim i perturbed)
    f_base   : f at the mid-point
    evals    : total unique evaluations dispatched
    """
    m = 0.5 * (x_lower + x_upper)   # base point

    # Build the complete list of probe tasks up-front.
    # Each task is (f, point, tag).
    tasks = []

    # Base point
    tasks.append((f, m.copy(), ("base",)))

    # Single-perturbation points: perturb only dimension i
    for i in range(n):
        x = m.copy()
        x[i] = x_upper[i]
        tasks.append((f, x, ("single", i)))

    # Double-perturbation points: perturb dimensions i and j
    for i in range(n - 1):
        for j in range(i + 1, n):
            x = m.copy()
            x[i] = x_upper[i]
            x[j] = x_upper[j]
            tasks.append((f, x, ("double", i, j)))

    evals = len(tasks)   # = 1 + n + n(n-1)/2  ==  n(n+1)/2 + 1  (lower bound)

    # Dispatch all tasks in parallel
    f_base = np.nan
    f_hat  = np.full(n, np.nan)
    F      = np.full((n, n), np.nan)

    with ProcessPoolExecutor(max_workers=n_workers) as pool:
        futures = {pool.submit(_eval_probe, task): task[2] for task in tasks}
        for future in as_completed(futures):
            tag, value = future.result()
            if tag[0] == "base":
                f_base = value
            elif tag[0] == "single":
                f_hat[tag[1]] = value
            else:  # "double"
                i, j = tag[1], tag[2]
                F[i, j] = value

    # Assemble Lambda — pure numpy, no more f calls
    Lambda = np.zeros((n, n))
    for i in range(n - 1):
        delta1_i = f_hat[i] - f_base          # delta1 at xj = mj
        for j in range(i + 1, n):
            delta2_i = F[i, j] - f_hat[j]    # delta2 at xj = upper
            lam = abs(delta1_i - delta2_i)
            Lambda[i, j] = lam
            Lambda[j, i] = lam               # symmetric

    return Lambda, F, f_hat, f_base, evals


# ---------------------------------------------------------------------------
# Step 2 - Design Structure Matrix  (Algorithm 3, unchanged from paper)
# ---------------------------------------------------------------------------

def _compute_DSM(
    Lambda: np.ndarray,
    F: np.ndarray,
    f_hat: np.ndarray,
    f_base: float,
    n: int,
) -> np.ndarray:
    """
    Convert the raw ISM (Lambda) into a binary Design Structure Matrix (Theta).

    Uses per-pair roundoff bounds einf and esup (Equations 3-7 in the paper)
    so the threshold epsilon is parameter-free and adapts to each pair of variables.

    Returns
    -------
    Theta : (n, n) int array;  Theta_ij = 1  means xi and xj interact.
    """
    gamma_sqrt_n = MACHINE_EPSILON * math.sqrt(n) / (1 - math.sqrt(n) * MACHINE_EPSILON)

    Theta = np.full((n, n), np.nan)
    eta0 = 0   # pairs reliably separable  (lam < einf)
    eta1 = 0   # pairs reliably interacting (lam > esup)

    # First pass: assign clear-cut 0s and 1s
    for i in range(n - 1):
        for j in range(i + 1, n):
            fij = float(F[i, j]) if not np.isnan(F[i, j]) else 0.0
            fi  = float(f_hat[i]) if not np.isnan(f_hat[i]) else 0.0
            fj  = float(f_hat[j]) if not np.isnan(f_hat[j]) else 0.0
            fb  = float(f_base)

            einf = GAMMA2 * max(abs(fb) + abs(fij), abs(fi) + abs(fj))
            esup = gamma_sqrt_n * max(abs(fb), abs(fij), abs(fi), abs(fj))
            lam  = Lambda[i, j]

            if lam < einf:
                Theta[i, j] = Theta[j, i] = 0
                eta0 += 1
            elif lam > esup:
                Theta[i, j] = Theta[j, i] = 1
                eta1 += 1
            # else: ambiguous — handled in second pass

    # Second pass: threshold ambiguous values with Equation (7)
    total_reliable = eta0 + eta1
    for i in range(n - 1):
        for j in range(i + 1, n):
            if not np.isnan(Theta[i, j]):
                continue

            fij = float(F[i, j]) if not np.isnan(F[i, j]) else 0.0
            fi  = float(f_hat[i]) if not np.isnan(f_hat[i]) else 0.0
            fj  = float(f_hat[j]) if not np.isnan(f_hat[j]) else 0.0
            fb  = float(f_base)

            einf = GAMMA2 * max(abs(fb) + abs(fij), abs(fi) + abs(fj))
            esup = gamma_sqrt_n * max(abs(fb), abs(fij), abs(fi), abs(fj))

            if total_reliable == 0:
                epsilon = 0.5 * (einf + esup)
            else:
                epsilon = (eta0 / total_reliable) * einf + (eta1 / total_reliable) * esup

            Theta[i, j] = Theta[j, i] = 1 if Lambda[i, j] > epsilon else 0

    np.fill_diagonal(Theta, 0)
    Theta = np.nan_to_num(Theta, nan=0.0)
    return Theta.astype(int)


# ---------------------------------------------------------------------------
# Step 3 - Connected Components  (BFS, linear in n)
# ---------------------------------------------------------------------------

def _connected_components(Theta: np.ndarray, n: int) -> tuple[list[list[int]], list[int]]:
    """
    Partition variables into nonseparable groups and fully separable singletons.

    Returns
    -------
    groups    : list of sorted lists of interacting gene indices
    separable : sorted list of singleton (separable) gene indices
    """
    visited = [False] * n
    groups, separable = [], []

    for start in range(n):
        if visited[start]:
            continue
        component, queue = [], [start]
        visited[start] = True
        while queue:
            node = queue.pop(0)
            component.append(node)
            for nb in range(n):
                if not visited[nb] and Theta[node, nb] == 1:
                    visited[nb] = True
                    queue.append(nb)

        if len(component) == 1:
            separable.append(component[0])
        else:
            groups.append(sorted(component))

    return groups, separable


