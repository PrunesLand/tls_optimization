"""
DG2: A Faster and More Accurate Differential Grouping for Large-Scale Black-Box Optimization
Implementation based on:
    Omidvar et al. (2017), IEEE Transactions on Evolutionary Computation, Vol. 21, No. 6

Parallelisation strategy
------------------------
Every unique probe point generated by the ISM step is independent — DG2 only
needs them all *collected* before DSM can run.  We therefore:

  1. Compute f_base on the midpoint (1 call, unavoidable serial dependency).
  2. Submit ALL remaining unique probe points (the n single-perturbation points
     + the n(n-1)/2 double-perturbation points) to a ProcessPoolExecutor in one
     batch, using every available CPU core.
  3. Collect results and assemble Λ, F, f_hat exactly as Algorithm 2 requires.

This turns what was O(n²) sequential SUMO launches into a single parallel
wave, saturating all cores for the bulk of the work.

Usage in traffic light optimisation
------------------------------------
Run DG2 before the genetic algorithm to identify which phase-duration genes
interact with each other.  Interacting genes should be optimised together;
separable genes can be treated independently.

Phase duration constraints
--------------------------
Phase type is inferred from the dominant character in the state string:
  Green  (G dominant) : gene bounds [24, 85],  min after clamping = 24 s
  Yellow (y dominant) : gene bounds [ 3,  6],  clamped to [3, 6] — NOT adjusted
  Red    (r dominant) : gene bounds [ 5, 85],  min after clamping =  5 s

Cycle length = 90 s.  Any remainder or shortfall after clamping is
absorbed by the green-or-red phase with the smallest current duration.
"""

import json
import math
import os
import sys
import time
import numpy as np
from concurrent.futures import ProcessPoolExecutor, as_completed
from pathlib import Path
from typing import Callable

