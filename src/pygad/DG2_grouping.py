def _toy_f(x: np.ndarray) -> float:
    """f(x) = (x0+x1)^2 + x2^2 + x3^2 + (x4+x5+x6)^2
    Expected groups: {0,1}, {4,5,6}   separable: {2}, {3}
    """
    return (x[0] + x[1])**2 + x[2]**2 + x[3]**2 + (x[4] + x[5] + x[6])**2


