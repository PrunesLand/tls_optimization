# Public API
# ---------------------------------------------------------------------------

def run_dg2(
    f: Callable[[np.ndarray], float],
    n: int,
    x_lower: np.ndarray,
    x_upper: np.ndarray,
    gene_labels: list[str] | None = None,
    output_path: str | Path = "dg2_results.json",
    n_workers: int | None = None,
    verbose: bool = True,
) -> dict:
    """
    Run the parallel DG2 algorithm to detect variable interactions.

    Parameters
    ----------
    f           : Black-box fitness function f(x) -> float.
                  IMPORTANT: must be picklable (i.e. a module-level function
                  or a class with __call__).  Use build_traffic_fitness_wrapper()
                  which returns the picklable TrafficFitnessWrapper class.
    n           : Number of decision variables (genes).
    x_lower     : Lower bounds array, shape (n,).
    x_upper     : Upper bounds array, shape (n,).
    gene_labels : Human-readable name for each gene.  Defaults to "gene_i".
    output_path : Destination for the JSON results file.
    n_workers   : Number of parallel worker processes.
                  Defaults to os.cpu_count() — uses every available core.
    verbose     : Print progress to stdout.

    Returns
    -------
    results dict with keys:
        groups                  — interacting groups (list of dicts)
        separable_genes         — fully separable genes (list of dicts)
        interaction_matrix      — n x n binary adjacency matrix
        lambda_matrix           — n x n raw |delta1 - delta2| values
        gene_labels             — gene name strings
        n_genes / n_groups / n_separable
        function_evaluations    — total f calls (= n(n+1)/2 + 1, the lower bound)
        n_workers_used          — actual worker count
        computation_time_seconds
        timestamp
    """
    if gene_labels is None:
        gene_labels = [f"gene_{i}" for i in range(n)]

    if n_workers is None:
        n_workers = os.cpu_count() or 1

    x_lower = np.asarray(x_lower, dtype=float)
    x_upper = np.asarray(x_upper, dtype=float)

    lower_bound_evals = n * (n + 1) // 2 + 1

    if verbose:
        print(f"[DG2] Starting parallel interaction detection")
        print(f"[DG2]   Genes          : {n}")
        print(f"[DG2]   Workers        : {n_workers} / {os.cpu_count()} available cores")
        print(f"[DG2]   Probe points   : {lower_bound_evals}  (theoretical lower bound)")

    t_start = time.perf_counter()

    # Step 1 — parallel ISM
    if verbose:
        print("[DG2] Step 1/3 — Dispatching all probe evaluations in parallel ...")
    Lambda, F, f_hat, f_base, evals = _compute_ISM_parallel(f, n, x_lower, x_upper, n_workers)

    # Step 2 — DSM (fast numpy, no f calls)
    if verbose:
        print("[DG2] Step 2/3 — Computing Design Structure Matrix (DSM) ...")
    Theta = _compute_DSM(Lambda, F, f_hat, f_base, n)

    # Step 3 — connected components (BFS)
    if verbose:
        print("[DG2] Step 3/3 — Extracting connected components ...")
    groups, separable = _connected_components(Theta, n)

    elapsed = time.perf_counter() - t_start

    # Human-readable output
    named_groups = [
        {
            "group_id": gid,
            "size": len(grp),
            "gene_indices": grp,
            "gene_labels": [gene_labels[i] for i in grp],
        }
        for gid, grp in enumerate(groups)
    ]
    named_separable = [
        {"gene_index": i, "gene_label": gene_labels[i]} for i in separable
    ]

    if verbose:
        print(f"\n[DG2] Results -----------------------------------------------")
        print(f"[DG2]   Total genes          : {n}")
        print(f"[DG2]   Workers used         : {n_workers}")
        print(f"[DG2]   Nonseparable groups  : {len(groups)}")
        print(f"[DG2]   Separable genes      : {len(separable)}")
        print(f"[DG2]   Function evaluations : {evals}  (lower bound = {lower_bound_evals})")
        print(f"[DG2]   Computation time     : {elapsed:.4f} s")
        print(f"[DG2] -----------------------------------------------------------")
        for g in named_groups:
            print(f"[DG2]   Group {g['group_id']} (size {g['size']}): {g['gene_labels']}")
        if named_separable:
            print(f"[DG2]   Separable : {[s['gene_label'] for s in named_separable]}")

    results = {
        "groups": named_groups,
        "separable_genes": named_separable,
        "interaction_matrix": Theta.tolist(),
        "lambda_matrix": Lambda.tolist(),
        "gene_labels": gene_labels,
        "n_genes": n,
        "n_groups": len(groups),
        "n_separable": len(separable),
        "function_evaluations": evals,
        "n_workers_used": n_workers,
        "computation_time_seconds": round(elapsed, 6),
        "timestamp": time.strftime("%Y-%m-%dT%H:%M:%S"),
    }

    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w") as fh:
        json.dump(results, fh, indent=4)

    if verbose:
        print(f"[DG2] Results saved -> {output_path}")

    return results


# ---------------------------------------------------------------------------
# Traffic-light integration helper
# ---------------------------------------------------------------------------

class TrafficFitnessWrapper:
    """
    Picklable wrapper around your traffic fitness function.

    Phase duration constraints (applied per TLS per call):
      - Green  (state dominant 'G'): minimum 24 s, bounds [24, 85]
      - Yellow (state dominant 'y'): clamped to [3, 6] s — NOT adjusted for remainder
      - Red    (state dominant 'r'): minimum  5 s, bounds [5, 85]

    After clamping all phases to their type bounds, the difference from the
    90 s cycle target is absorbed by the green-or-red phase with the smallest
    current duration (yellow phases are never touched in this step).

    A plain closure (lambda or def inside a function) is NOT picklable and
    cannot cross process boundaries.  A class with __call__ IS picklable as
    long as all its attributes are also picklable.
    """

    def __init__(self, fitness_function, tls_mapping):
        self.fitness_function = fitness_function
        self.tls_mapping      = tls_mapping

    def __call__(self, vector: np.ndarray) -> float:
        tls_durations = {}

        for tls in self.tls_mapping:
            tls_id      = tls["tls_id"]
            phase_types = tls["phase_types"]   # list of "green"/"yellow"/"red"
            raw         = list(vector[tls["start_idx"]: tls["end_idx"]])
            n_phases    = len(raw)

            # ----------------------------------------------------------------
            # Step 1 — clamp each gene to its per-type bounds
            # ----------------------------------------------------------------
            durations = []
            for raw_val, ptype in zip(raw, phase_types):
                lo, hi = PHASE_BOUNDS[ptype]
                durations.append(int(round(max(lo, min(hi, raw_val)))))

            # ----------------------------------------------------------------
            # Step 2 — adjust for cycle-length target (90 s)
            #
            # Yellow phases are fixed after clamping — only green and red
            # absorb the remainder.  We find the green-or-red phase with the
            # smallest current duration and add/subtract there.
            # ----------------------------------------------------------------
            remainder = CYCLE_LENGTH - sum(durations)

            if remainder != 0:
                # Indices of adjustable (green or red) phases
                adjustable = [
                    i for i, ptype in enumerate(phase_types)
                    if ptype in ("green", "red")
                ]

                if adjustable:
                    # Pick the adjustable phase with the smallest current duration
                    target_idx = min(adjustable, key=lambda i: durations[i])
                    durations[target_idx] += remainder

                    # Safety: enforce minimum even after adjustment
                    lo, _ = PHASE_BOUNDS[phase_types[target_idx]]
                    if durations[target_idx] < lo:
                        # Distribute the minimum-violation back to the largest
                        # adjustable phase to avoid an infeasible solution
                        durations[target_idx] = lo
                        fallback = max(adjustable, key=lambda i: durations[i])
                        durations[fallback] += CYCLE_LENGTH - sum(durations)

            tls_durations[tls_id] = durations

        return self.fitness_function(tls_durations)


def build_traffic_fitness_wrapper(
    baseline_data: dict,
    fitness_function: Callable[[dict], float],
) -> tuple[Callable[[np.ndarray], float], int, np.ndarray, np.ndarray, list[str]]:
    """
    Build a picklable fitness wrapper compatible with multiprocessing.

    Derives the TLS mapping (including per-phase type from the state string)
    directly from *baseline_data*, so the caller only needs to supply the
    loaded JSON.

    Gene bounds are set per phase type:
      green  -> [24, 85]
      yellow -> [ 3,  6]
      red    -> [ 5, 85]

    Returns
    -------
    f        : TrafficFitnessWrapper instance (picklable, safe for DG2 workers)
    n        : number of genes
    x_lower  : lower bounds per gene (type-aware)
    x_upper  : upper bounds per gene (type-aware, all 85 except yellow capped at 6)
    labels   : gene names, e.g. ["186797066_phase_1", "186797066_phase_2", ...]
    """
    tls_mapping = []
    gene_idx    = 0
    x_lower_list: list[float] = []
    x_upper_list: list[float] = []
    labels:       list[str]   = []

    for tls_id in sorted(baseline_data["tls_data"].keys()):
        phase_keys  = sorted(baseline_data["tls_data"][tls_id].keys())
        phase_types = []

        for pk in phase_keys:
            state = baseline_data["tls_data"][tls_id][pk].get("state", "")
            ptype = _phase_type(state)
            phase_types.append(ptype)

            lo, hi = PHASE_BOUNDS[ptype]
            x_lower_list.append(float(lo))
            x_upper_list.append(float(hi))
            labels.append(f"{tls_id}_{pk}")

        tls_mapping.append({
            "tls_id":      tls_id,
            "num_phases":  len(phase_keys),
            "phase_types": phase_types,
            "start_idx":   gene_idx,
            "end_idx":     gene_idx + len(phase_keys),
        })
        gene_idx += len(phase_keys)

    n       = gene_idx
    x_lower = np.array(x_lower_list, dtype=float)
    x_upper = np.array(x_upper_list, dtype=float)

    f = TrafficFitnessWrapper(fitness_function, tls_mapping)
    return f, n, x_lower, x_upper, labels


